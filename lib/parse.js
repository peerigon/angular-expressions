function stringify(value) {
	if (value == null) {
		// null || undefined
		return "";
	}
	switch (typeof value) {
		case "string":
			break;
		case "number":
			value = "" + value;
			break;
		default:
			if (hasCustomToString(value) && !isArray(value) && !isDate(value)) {
				value = value.toString();
			} else {
				value = toJson(value);
			}
	}

	return value;
}

